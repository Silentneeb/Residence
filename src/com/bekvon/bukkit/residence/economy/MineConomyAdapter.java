package com.bekvon.bukkit.residence.economy;
import com.spikensbror.bukkit.mineconomy.MineConomy;

public class MineConomyAdapter implements EconomyInterface
{
    private MineConomy plugin;
    
    public MineConomyAdapter(MineConomy p)
    {
        plugin = p;
    }

    public double getBalance(String playerName)
    {
        return plugin.getBank().getTotal(playerName);
    }

    public boolean canAfford(String playerName, double amount)
    {
        return plugin.getBank().getTotal(playerName) >= amount;
    }

    public boolean add(String playerName, double amount)
    {
        return plugin.getBank().add(playerName, amount);
    }

    public boolean subtract(String playerName, double amount)
    {
        return plugin.getBank().subtract(playerName, amount);
    }

    public boolean transfer(String playerFrom, String playerTo, double amount)
    {
        return plugin.getBank().transfer(playerFrom, playerTo, amount);
    }
    
    public String getName()
    {
        return "MineConomy";
    }
}
